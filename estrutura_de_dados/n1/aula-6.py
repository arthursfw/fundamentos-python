# Manipulação de Strings e Índices em Python

nome_do_personagem = 'Romario'

# Converte a string para letra maiúscula
nome_do_personagem_maiusculo = nome_do_personagem.upper()
print(nome_do_personagem_maiusculo)

# Converte a string para letra minúscula
nome_minusculo = nome_do_personagem.lower()
print(nome_minusculo)

# Imprime o comprimento (número de caracteres) da string
print(len(nome_do_personagem))

# Imprime os primeiros 5 caracteres da string (array de caracteres)
print(nome_do_personagem[0:5])

# Imprime o último caractere (assim por diante)
print(nome_do_personagem[-1])

# Índice: Posição de um elemento em uma sequência.
# String: Array de caracteres.